import React, { useState, useEffect } from 'react';
import { getServerUrl } from '../utils/supabase/client';
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from './ui/card';
import { Button } from './ui/button';
import { Badge } from './ui/badge';
import { ScrollArea } from './ui/scroll-area';
import { Separator } from './ui/separator';
import { 
  CheckCircle2, 
  XCircle, 
  Clock, 
  Calendar,
  ArrowLeft,
  FileText,
  Award,
  AlertCircle
} from 'lucide-react';
import { format } from 'date-fns';
import { toast } from 'sonner@2.0.3';

interface Assessment {
  id: string;
  title: string;
  description: string;
  questions: Question[];
}

interface Question {
  id?: string;
  question: string;
  options: string[];
  correctAnswer: number;
  type?: string;
}

interface Submission {
  id: string;
  userId: string;
  assessmentId: string;
  answers: { [key: number]: number };
  score: number;
  totalQuestions: number;
  submittedAt: string;
  assessment?: Assessment;
}

interface ReviewAssessmentsProps {
  session: any;
  userId: string;
}

export function ReviewAssessments({ session, userId }: ReviewAssessmentsProps) {
  const [submissions, setSubmissions] = useState<Submission[]>([]);
  const [selectedSubmission, setSelectedSubmission] = useState<Submission | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    fetchSubmissions();
  }, [userId]);

  const fetchSubmissions = async () => {
    try {
      setLoading(true);
      
      const response = await fetch(getServerUrl(`/submissions/${userId}`), {
        headers: {
          'Authorization': `Bearer ${session.access_token}`,
        },
      });

      if (!response.ok) throw new Error('Failed to fetch submissions');
      
      const data = await response.json();
      setSubmissions(data.submissions || []);
    } catch (error: any) {
      console.error('Error fetching submissions:', error);
      toast.error('Failed to load past assessments');
    } finally {
      setLoading(false);
    }
  };

  const getPercentage = (score: number, maxScore: number) => {
    return maxScore > 0 ? Math.round((score / maxScore) * 100) : 0;
  };

  const getScoreColor = (percentage: number) => {
    if (percentage >= 90) return 'text-green-600';
    if (percentage >= 70) return 'text-blue-600';
    if (percentage >= 50) return 'text-yellow-600';
    return 'text-red-600';
  };

  const getScoreBadgeVariant = (percentage: number): "default" | "secondary" | "destructive" | "outline" => {
    if (percentage >= 90) return 'default';
    if (percentage >= 70) return 'secondary';
    if (percentage >= 50) return 'outline';
    return 'destructive';
  };

  if (loading) {
    return (
      <div className="flex items-center justify-center h-64">
        <div className="text-center">
          <div className="animate-spin rounded-full h-12 w-12 border-b-2 border-blue-600 mx-auto mb-4"></div>
          <p className="text-muted-foreground">Loading your assessment history...</p>
        </div>
      </div>
    );
  }

  // Review view - showing individual assessment with answers
  if (selectedSubmission && selectedSubmission.assessment) {
    const assessment = selectedSubmission.assessment;
    const percentage = getPercentage(selectedSubmission.score, selectedSubmission.totalQuestions);
    
    // Convert answers object to array for easier processing
    const answersArray = Object.keys(selectedSubmission.answers || {})
      .sort((a, b) => Number(a) - Number(b))
      .map(key => selectedSubmission.answers[Number(key)]);
    
    const correctCount = answersArray.filter(
      (answer, index) => answer === assessment.questions[index]?.correctAnswer
    ).length;
    const incorrectCount = assessment.questions.length - correctCount;

    return (
      <div className="space-y-6">
        {/* Header */}
        <div className="bg-gradient-to-r from-purple-500 via-pink-500 to-rose-500 rounded-lg shadow-lg p-8 text-white">
          <Button
            variant="ghost"
            onClick={() => setSelectedSubmission(null)}
            className="mb-4 text-white hover:bg-white/20"
          >
            <ArrowLeft className="mr-2 h-4 w-4" />
            Back to All Assessments
          </Button>
          
          <div className="flex items-start justify-between">
            <div className="flex-1">
              <h1 className="text-3xl mb-2">{assessment.title}</h1>
              {assessment.description && (
                <p className="text-white/90 mb-4">{assessment.description}</p>
              )}
              <div className="flex items-center gap-4 text-sm text-white/80">
                <div className="flex items-center gap-1">
                  <Calendar className="h-4 w-4" />
                  <span>Submitted: {format(new Date(selectedSubmission.submittedAt), 'MMM dd, yyyy')}</span>
                </div>
                <div className="flex items-center gap-1">
                  <Clock className="h-4 w-4" />
                  <span>{format(new Date(selectedSubmission.submittedAt), 'h:mm a')}</span>
                </div>
              </div>
            </div>
            
            <div className="text-right">
              <div className="bg-white/20 backdrop-blur-sm rounded-lg p-4">
                <div className="text-4xl mb-1">{percentage}%</div>
                <div className="text-sm text-white/80">
                  {selectedSubmission.score} / {selectedSubmission.totalQuestions} points
                </div>
              </div>
            </div>
          </div>
        </div>

        {/* Summary Stats */}
        <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
          <Card>
            <CardContent className="pt-6">
              <div className="flex items-center gap-4">
                <div className="h-12 w-12 rounded-full bg-green-100 flex items-center justify-center">
                  <CheckCircle2 className="h-6 w-6 text-green-600" />
                </div>
                <div>
                  <div className="text-2xl">{correctCount}</div>
                  <div className="text-sm text-muted-foreground">Correct Answers</div>
                </div>
              </div>
            </CardContent>
          </Card>

          <Card>
            <CardContent className="pt-6">
              <div className="flex items-center gap-4">
                <div className="h-12 w-12 rounded-full bg-red-100 flex items-center justify-center">
                  <XCircle className="h-6 w-6 text-red-600" />
                </div>
                <div>
                  <div className="text-2xl">{incorrectCount}</div>
                  <div className="text-sm text-muted-foreground">Incorrect Answers</div>
                </div>
              </div>
            </CardContent>
          </Card>

          <Card>
            <CardContent className="pt-6">
              <div className="flex items-center gap-4">
                <div className="h-12 w-12 rounded-full bg-blue-100 flex items-center justify-center">
                  <Award className="h-6 w-6 text-blue-600" />
                </div>
                <div>
                  <div className="text-2xl">{assessment.questions.length}</div>
                  <div className="text-sm text-muted-foreground">Total Questions</div>
                </div>
              </div>
            </CardContent>
          </Card>
        </div>

        {/* Questions Review */}
        <Card>
          <CardHeader>
            <CardTitle>Detailed Review</CardTitle>
            <CardDescription>
              Review each question to understand what you got right and what needs improvement
            </CardDescription>
          </CardHeader>
          <CardContent>
            <ScrollArea className="h-[600px] pr-4">
              <div className="space-y-6">
                {assessment.questions.map((question, index) => {
                  const userAnswer = answersArray[index];
                  const isCorrect = userAnswer === question.correctAnswer;
                  const hasAnswer = userAnswer !== undefined && userAnswer !== null;

                  return (
                    <div key={index} className="space-y-3">
                      {/* Question Header */}
                      <div className="flex items-start gap-3">
                        <div className={`flex-shrink-0 h-8 w-8 rounded-full flex items-center justify-center ${
                          !hasAnswer ? 'bg-gray-100' :
                          isCorrect ? 'bg-green-100' : 'bg-red-100'
                        }`}>
                          {!hasAnswer ? (
                            <AlertCircle className="h-5 w-5 text-gray-600" />
                          ) : isCorrect ? (
                            <CheckCircle2 className="h-5 w-5 text-green-600" />
                          ) : (
                            <XCircle className="h-5 w-5 text-red-600" />
                          )}
                        </div>
                        <div className="flex-1">
                          <div className="flex items-start justify-between gap-4">
                            <h4 className="font-medium">
                              Question {index + 1}
                            </h4>
                            <Badge variant={
                              !hasAnswer ? 'outline' :
                              isCorrect ? 'default' : 'destructive'
                            }>
                              {!hasAnswer ? 'Not Answered' : isCorrect ? 'Correct' : 'Incorrect'}
                            </Badge>
                          </div>
                          <p className="text-muted-foreground mt-1">{question.question}</p>
                        </div>
                      </div>

                      {/* Options */}
                      <div className="ml-11 space-y-2">
                        {question.options.map((option, optionIndex) => {
                          const isUserAnswer = userAnswer === optionIndex;
                          const isCorrectAnswer = question.correctAnswer === optionIndex;
                          
                          let optionClass = 'border-2 p-3 rounded-lg transition-colors';
                          
                          if (isCorrectAnswer) {
                            optionClass += ' border-green-500 bg-green-50';
                          } else if (isUserAnswer && !isCorrect) {
                            optionClass += ' border-red-500 bg-red-50';
                          } else {
                            optionClass += ' border-gray-200 bg-gray-50';
                          }

                          return (
                            <div key={optionIndex} className={optionClass}>
                              <div className="flex items-center justify-between">
                                <span className={isCorrectAnswer || isUserAnswer ? 'font-medium' : ''}>
                                  {String.fromCharCode(65 + optionIndex)}. {option}
                                </span>
                                <div className="flex items-center gap-2">
                                  {isUserAnswer && (
                                    <Badge variant="outline" className="text-xs">
                                      Your Answer
                                    </Badge>
                                  )}
                                  {isCorrectAnswer && (
                                    <Badge variant="default" className="text-xs bg-green-600">
                                      Correct Answer
                                    </Badge>
                                  )}
                                </div>
                              </div>
                            </div>
                          );
                        })}
                      </div>

                      {/* Explanation for incorrect/unanswered */}
                      {(!isCorrect || !hasAnswer) && (
                        <div className="ml-11 p-4 bg-blue-50 border border-blue-200 rounded-lg">
                          <div className="flex items-start gap-2">
                            <AlertCircle className="h-5 w-5 text-blue-600 flex-shrink-0 mt-0.5" />
                            <div>
                              <p className="font-medium text-blue-900 mb-1">Learning Point</p>
                              <p className="text-sm text-blue-800">
                                The correct answer is <strong>{String.fromCharCode(65 + question.correctAnswer)}. {question.options[question.correctAnswer]}</strong>. 
                                {!hasAnswer && ' You did not answer this question.'} Review this topic in your learning materials to strengthen your understanding.
                              </p>
                            </div>
                          </div>
                        </div>
                      )}

                      {index < assessment.questions.length - 1 && (
                        <Separator className="my-6" />
                      )}
                    </div>
                  );
                })}
              </div>
            </ScrollArea>
          </CardContent>
        </Card>
      </div>
    );
  }

  // List view - showing all submissions
  return (
    <div className="space-y-6">
      {/* Header */}
      <div className="bg-gradient-to-r from-blue-500 via-indigo-500 to-purple-500 rounded-lg shadow-lg p-8 text-white">
        <div className="flex items-center gap-3 mb-2">
          <FileText className="h-8 w-8" />
          <h1 className="text-3xl">Review Past Assessments</h1>
        </div>
        <p className="text-white/90">
          Review your submitted assessments and learn from your mistakes
        </p>
      </div>

      {/* Submissions List */}
      {submissions.length === 0 ? (
        <Card>
          <CardContent className="pt-6">
            <div className="text-center py-12">
              <FileText className="h-12 w-12 text-muted-foreground mx-auto mb-4" />
              <h3 className="text-lg font-medium mb-2">No Assessments Yet</h3>
              <p className="text-muted-foreground">
                You haven't submitted any assessments yet. Complete an assessment to see it here!
              </p>
            </div>
          </CardContent>
        </Card>
      ) : (
        <div className="grid gap-4">
          {submissions.map((submission) => {
            const percentage = getPercentage(submission.score, submission.totalQuestions);
            const assessment = submission.assessment;

            return (
              <Card 
                key={submission.id} 
                className="hover:shadow-md transition-shadow cursor-pointer"
                onClick={() => setSelectedSubmission(submission)}
              >
                <CardContent className="pt-6">
                  <div className="flex items-start justify-between gap-4">
                    <div className="flex-1">
                      <div className="flex items-center gap-3 mb-2">
                        <h3 className="text-lg font-medium">
                          {assessment?.title || 'Assessment'}
                        </h3>
                        <Badge variant={getScoreBadgeVariant(percentage)}>
                          {percentage}%
                        </Badge>
                      </div>
                      
                      {assessment?.description && (
                        <p className="text-sm text-muted-foreground mb-3">
                          {assessment.description}
                        </p>
                      )}

                      <div className="flex items-center gap-4 text-sm text-muted-foreground">
                        <div className="flex items-center gap-1">
                          <Calendar className="h-4 w-4" />
                          <span>{format(new Date(submission.submittedAt), 'MMM dd, yyyy')}</span>
                        </div>
                        <div className="flex items-center gap-1">
                          <Clock className="h-4 w-4" />
                          <span>{format(new Date(submission.submittedAt), 'h:mm a')}</span>
                        </div>
                      </div>
                    </div>

                    <div className="text-right space-y-2">
                      <div className={`text-3xl ${getScoreColor(percentage)}`}>
                        {submission.score}/{submission.totalQuestions}
                      </div>
                      <div className="text-sm text-muted-foreground">
                        {assessment?.questions.length || 0} questions
                      </div>
                      <Button size="sm" className="w-full">
                        Review Answers
                      </Button>
                    </div>
                  </div>

                  {/* Progress Bar */}
                  <div className="mt-4">
                    <div className="w-full bg-gray-200 rounded-full h-2">
                      <div
                        className={`h-2 rounded-full transition-all ${
                          percentage >= 90 ? 'bg-green-600' :
                          percentage >= 70 ? 'bg-blue-600' :
                          percentage >= 50 ? 'bg-yellow-600' :
                          'bg-red-600'
                        }`}
                        style={{ width: `${percentage}%` }}
                      />
                    </div>
                  </div>
                </CardContent>
              </Card>
            );
          })}
        </div>
      )}
    </div>
  );
}
